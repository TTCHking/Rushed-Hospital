using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.Events;

public class Timer : MonoBehaviour
{
    public UnityEvent OnTimerStart;
    public UnityEvent OnTimeOut;
    public float startTime; //?????????????
    public float timerCount; //?????????????????????

    public bool isStartTimer = false;

    public void Start()
    {
        StartTimer();
    }
    public void StartTimer()
    {
        OnTimerStart.Invoke(); //invoke????????????? empty list ?? inspector ???????????????????????????????????????????????????????  

        isStartTimer = true;
        timerCount = startTime;
    }

    private void FixedUpdate()
    {
        if (timerCount > 0 && isStartTimer)
        {
            timerCount -= Time.deltaTime;

            if (timerCount < 0)
            {
                OnTimeOut.Invoke(); //invoke????????????? empty list ?? inspector ???????????????????????????????????????????????????????  
            }

        }
    }

    public void TimerStartEvent()
    {
        Debug.Log("StartWorld");
    }

    public void TimerEndEvent()
    {
        Debug.Log("GameOver");
    }
}
